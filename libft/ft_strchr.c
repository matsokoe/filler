#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*buf;

	i = -1;
	buf = (char *)s;
	while (buf[++i] != c)
		if (buf[i] == '\0')
			return (0);
	return (&buf[i]);
}
