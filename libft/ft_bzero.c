#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*buf_s;
	size_t			i;

	i = 0;
	buf_s = (unsigned char *)s;
	while (i < n)
	{
		buf_s[i] = 0;
		i++;
	}
}
